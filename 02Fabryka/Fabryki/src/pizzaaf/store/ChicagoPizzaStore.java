package pizzaaf.store;

import pizzaaf.ingredientfactory.ChicagoPizzaIngredientFactory;
import pizzaaf.ingredientfactory.PizzaIngredientFactory;
import pizzaaf.pizza.CheesePizza;
import pizzaaf.pizza.ClamPizza;
import pizzaaf.pizza.PepperoniPizza;
import pizzaaf.pizza.Pizza;
import pizzaaf.pizza.VeggiePizza;

public class ChicagoPizzaStore extends PizzaStore {

	protected Pizza createPizza(String item) {
		Pizza pizza = null;
		PizzaIngredientFactory ingredientFactory =
		new ChicagoPizzaIngredientFactory();

		if (item.equals("cheese")) {

			pizza = new CheesePizza(ingredientFactory);
			pizza.setName("Chicago Style Cheese Pizza");

		} else if (item.equals("veggie")) {

			pizza = new VeggiePizza(ingredientFactory);
			pizza.setName("Chicago Style Veggie Pizza");

		} else if (item.equals("clam")) {

			pizza = new ClamPizza(ingredientFactory);
			pizza.setName("Chicago Style Clam Pizza");

		} else if (item.equals("pepperoni")) {

			pizza = new PepperoniPizza(ingredientFactory);
			pizza.setName("Chicago Style Pepperoni Pizza");

		}
		return pizza;
	}
}
